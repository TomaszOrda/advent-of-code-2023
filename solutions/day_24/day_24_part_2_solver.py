from sympy import symbols, Eq, solve
t0, t1, t2, v0, v1, v2, c0, c1, c2 = symbols('t0 t1 t2 v0 v1 v2 c0 c1 c2', integer=True)
eq0 = Eq(t0*v0+c0, t0*(-2)+(19))
eq1 = Eq(t0*v1+c1, t0*(1)+(13))
eq2 = Eq(t0*v2+c2, t0*(-2)+(30))
eq3 = Eq(t1*v0+c0, t1*(-1)+(18))
eq4 = Eq(t1*v1+c1, t1*(-1)+(19))
eq5 = Eq(t1*v2+c2, t1*(-2)+(22))
eq6 = Eq(t2*v0+c0, t2*(-2)+(20))
eq7 = Eq(t2*v1+c1, t2*(-2)+(25))
eq8 = Eq(t2*v2+c2, t2*(-4)+(34))
solutions = solve([eq0, eq1, eq2, eq3, eq4, eq5, eq6, eq7, eq8], (c0, c1, c2, t0, t1, t2, v0, v1, v2))
print(solutions)
print(sum(solutions[0][0:3]))
